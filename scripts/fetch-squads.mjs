const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

async function jget(url, attempt = 1) {
  const r = await fetch(url, { headers: HEADERS });
  if (r.status === 429) {
    if (attempt > 3) throw new Error(`429 after ${attempt} tries`);
    console.log(`  …rate-limited, sleeping ${RETRY_WAIT_MS / 1000}s`);
    await sleep(RETRY_WAIT_MS);
    return jget(url, attempt + 1);
  }
  if (!r.ok) throw new Error(`${r.status} ${url}`);
  return r.json();
}

async function resolveTeamId(name) {
  const q = name.replace(/-/g, " ");
  const d = await jget(`${BASE}/teams?search=${encodeURIComponent(q)}`);
  const match = (d.response ?? []).find(
    (t) => t.team?.national === true && t.team?.name?.toLowerCase() === q.toLowerCase(),
  );
  return match?.team?.id ?? null;
}

async function fetchSquad(teamId) {
  const d = await jget(`${BASE}/players/squads?team=${teamId}`);
  const entry = d.response?.[0];
  if (!entry) return null;
  return {
    team: { id: entry.team.id, name: entry.team.name, logo: entry.team.logo },
    players: entry.players.map((p) => ({
      id: p.id,
      number: p.number,
      position: p.position,
      name: p.name,
      age: p.age,
      photo: p.photo,
    })),
  };
}

function loadExisting() {
  if (!existsSync(OUT_PATH)) return {};
  const text = readFileSync(OUT_PATH, "utf8");
  const m = text.match(/=\s*({[\s\S]*});\s*$/);
  if (!m) return {};
  try {
    return JSON.parse(m[1]);
  } catch {
    return {};
  }
}

function serialise(map) {
  const header =
    `// AUTO-GENERATED by scripts/fetch-squads.mjs — do not edit by hand.\n` +
    `// Run \`node scripts/fetch-squads.mjs\` to refresh.\n\n` +
    `import type { Squad } from "@/types";\n\n` +
    `export const SQUADS: Record<string, Squad> = `;
  const body = JSON.stringify(map, null, 2);
  return `${header}${body};\n`;
}

async function main() {
  const out = loadExisting();
  const alreadyHave = new Set(Object.keys(out));
  if (alreadyHave.size) console.log(`resuming — already have: ${[...alreadyHave].join(", ")}`);

  let calls = 0;
  for (const c of COUNTRIES) {
    if (alreadyHave.has(c.code)) continue;
    try {
      let teamId = c.teamId;
      if (!teamId) {
        teamId = await resolveTeamId(c.name);
        calls += 1;
        await sleep(REQ_GAP_MS);
        if (!teamId) {
          console.warn(`skip ${c.code} (${c.name}) — no national team found`);
          continue;
        }
        console.log(`resolved ${c.code} -> ${teamId}`);
      }
      const squad = await fetchSquad(teamId);
      calls += 1;
      if (!squad) {
        console.warn(`skip ${c.code} — empty squad`);
      } else {
        out[c.code] = {
          code: c.code,
          country: c.name.replace(/-/g, " "),
          teamId,
          teamName: squad.team.name,
          logo: squad.team.logo,
          players: squad.players,
        };
        console.log(`ok   ${c.code} (${squad.players.length} players)`);
        writeFileSync(OUT_PATH, serialise(out));
      }
      await sleep(REQ_GAP_MS);
    } catch (e) {
      console.error(`fail ${c.code}:`, e.message);
      writeFileSync(OUT_PATH, serialise(out));
    }
  }
  mkdirSync(dirname(OUT_PATH), { recursive: true });
  writeFileSync(OUT_PATH, serialise(out));
  console.log(`\nwrote ${OUT_PATH}`);
  console.log(`new api calls this run: ${calls}; total squads stored: ${Object.keys(out).length}`);
}

main();
